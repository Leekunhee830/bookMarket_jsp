package com.bookmarket.dto.cart;

public class CartAllDto {
	private int user_num;
	private int pd_num;
	private String pd_name;
	private String pd_manufacturer;
	private int pd_price;
	private String pd_img;
	private String regdate;
	
	
	public int getPd_num() {
		return pd_num;
	}
	public void setPd_num(int pd_num) {
		this.pd_num = pd_num;
	}
	public String getRegdate() {
		return regdate;
	}
	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}
	public int getUser_num() {
		return user_num;
	}
	public void setUser_num(int user_num) {
		this.user_num = user_num;
	}
	public String getPd_name() {
		return pd_name;
	}
	public void setPd_name(String pd_name) {
		this.pd_name = pd_name;
	}
	public String getPd_manufacturer() {
		return pd_manufacturer;
	}
	public void setPd_manufacturer(String pd_manufacturer) {
		this.pd_manufacturer = pd_manufacturer;
	}
	public int getPd_price() {
		return pd_price;
	}
	public void setPd_price(int pd_price) {
		this.pd_price = pd_price;
	}
	public String getPd_img() {
		return pd_img;
	}
	public void setPd_img(String pd_img) {
		this.pd_img = pd_img;
	}

	
}
